export const getArticlesByDate = (date: Date): Article[] => {
  const dateStr = date.toISOString().split('T')[0];
  return mockArticles.filter(article => 
    article.publishedAt.startsWith(dateStr)
  );
};

export const getCalendarEvents = (): { date: string; count: number }[] => {
  const events: { [key: string]: number } = {};
  
  mockArticles.forEach(article => {
    const date = article.publishedAt.split('T')[0];
    events[date] = (events[date] || 0) + 1;
  });
  
  return Object.entries(events).map(([date, count]) => ({ date, count }));
};
